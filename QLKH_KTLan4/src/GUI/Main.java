    public void Biding(){
        listKH=khDll.getALL();
        Vector clums=new Vector();
        clums.add("User");
        clums.add("Pass");
        clums.add("Full Name");
        clums.add("Age");
        clums.add("Address");
        clums.add("Phone");
        clums.add("CMND");
        clums.add("Phòng");
        clums.add("DỊch vụ");
        Vector data=new Vector();
        for(Khachhang emp : listKH){
            Vector row=new Vector();
            row.add(emp.getUsername());
            row.add(emp.getPassword());
            row.add(emp.getFullname());
            row.add(emp.getAge());
            row.add(emp.getAddress());
            row.add(emp.getPhone());
            row.add(emp.getCmnd());
            listP=pDLL.getByID(emp.getpID());
            if(listP.size()>0){
                row.add(listP.get(0).getpName());
            }else{
                row.add("is Empty");
            }
            
            listD=dDLL.getByID(emp.getdID());
            if(listD.size()>0){
                row.add(listD.get(0).getdName());
            }else{
                row.add("is Empty");
            }
            data.add(row);
        }
        DefaultTableModel dtm=new DefaultTableModel(data,clums);
        tbl.setModel(dtm);
    }
