    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:
        if(tbl.getSelectedRow()!=-1){
            int row=tbl.getSelectedRow();
            listKH=khDll.getALL();
            txt_username.setText(listKH.get(row).getUsername());
            txt_password.setText(listKH.get(row).getPassword());
            txt_fullname.setText(listKH.get(row).getFullname());
            txt_age.setText(listKH.get(row).getAge());
            txt_address.setText(listKH.get(row).getAddress());
            txt_phone.setText(listKH.get(row).getPhone());
            txt_CMND.setText(listKH.get(row).getCmnd());
            
            listD=dDLL.getByID(listKH.get(row).getdID());
            if(listD.size()>0){
                cbx_dID.setSelectedItem(listD.get(0).getdName());
            }else{
                cbx_dID.setSelectedIndex(0);
            }

            listP=pDLL.getByID(listKH.get(row).getpID());
            if(listP.size()>0){
                cbx_pID.setSelectedItem(listP.get(0).getpName());
            }else{
                cbx_pID.setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_tblMouseClicked

